public class Char_and_boolean {
    public static void main(String[] args){
        char myChar = 'D';
        
        System.out.println("Char: " + myChar);

        char myUnitcode = '\u0044';

        System.out.println("unitcode: " + myUnitcode);
        
        char myDecimalCode = 68;

        System.out.println("decimal code:" + myDecimalCode);

        char Questionchar = '?';
        char QuestionUnitCode = '\u003f';
        char QuestionCharCode = 63;

        System.out.println("question mark: " + Questionchar + " " + QuestionUnitCode + " " + QuestionCharCode);

        boolean myTrueBooleanValue = true;
        boolean MyFalseBooleanValue = false;

        System.out.println("true boolean value: " + myTrueBooleanValue);
        System.out.println("false boolean value: " + MyFalseBooleanValue);
        
    }
}
